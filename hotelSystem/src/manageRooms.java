    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 
              try {
                  
                //get table model
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                if(jTable1.getSelectedRowCount() == 1){

                 //get update values from the textfields
                String roomNum = jTextField1.getText();
                String roomPrice = jTextField2.getText();
                
                //set update value to the rows
                tblModel.setValueAt(roomNum, jTable1.getSelectedRow(), 0);
            
                String roomType = jComboBox1.getSelectedItem().toString();
                tblModel.setValueAt(roomType,jTable1.getSelectedRow(), 1);
            
                String bedType = jComboBox2.getSelectedItem().toString();
                tblModel.setValueAt(bedType,jTable1.getSelectedRow(), 2);
            
                tblModel.setValueAt(roomPrice,jTable1.getSelectedRow(), 3);
                }
                else{

                    if(jTable1.getSelectedRowCount() == 0){
                    JOptionPane.showMessageDialog(this,"Table is Empty!");
                    }
                    else{
                    JOptionPane.showMessageDialog(this,"Please Select Single Row!");
                }

               }
                  
                String dbroomNum = jTextField1.getText();
                String dbroomPrice = jTextField2.getText();
                String dbroomType = jComboBox1.getSelectedItem().toString();
                String dbbedType = jComboBox2.getSelectedItem().toString();
                
                //database
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/newhotel","root","");
                PreparedStatement ps=con.prepareStatement("UPDATE managerooms set roomType=?,bedType=?,roomPrice=?,status=? where roomNum=?");
                ps.setString(1, dbroomType);
                ps.setString(2, dbbedType);
                ps.setString(3, dbroomPrice);
                ps.setString(4, "Available");
                ps.setString(5, dbroomNum);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"Update Successfull!");
                
        } catch (SQLException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
            
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String deleteRoomNum = jTextField1.getText();
        DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();

        //delete row
        if(jTable1.getSelectedRowCount() == 1){
            tblModel.removeRow(jTable1.getSelectedRow());
        }
        else{
            if(jTable1.getSelectedRowCount() == 0){
                JOptionPane.showMessageDialog(this,"Table is Empty!");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please Select Single Row!");
            }
        }
        
          
         try {
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/newhotel","root","");
            PreparedStatement ps=con.prepareStatement("Delete from managerooms where roomNum=?");
            ps.setString(1,deleteRoomNum);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Delete Successfully");
            
        } catch (SQLException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

