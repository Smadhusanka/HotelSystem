    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         
    try {
            userName = jTextField1.getText();
            email = jTextField2.getText();
            mobile = Integer.parseInt(jTextField3.getText());
            password = jPasswordField1.getText();
            ConPassword = jPasswordField2.getText();
            
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/newhotel","root","");
            
            PreparedStatement ps=con.prepareStatement("insert into registerdetails(userName,email,mobile,gender,password)"
                    + "values(?,?,?,?,?)");
            ps.setString(1,userName);
            ps.setString(2, email);
            ps.setInt(3, mobile);
            if(jRadioButton1.isSelected())
            {
                ps.setString(4, jRadioButton1.getText());
            }
            else
            {
                 ps.setString(4, jRadioButton2.getText());
            }
            
           
            if(password.equals(ConPassword))
            {
                ps.setString(5, password);
            }
            else
            {
                JOptionPane.showMessageDialog(this,"password does not match!");
                return;
            }
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Register Successfully");
            
        } catch (SQLException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

