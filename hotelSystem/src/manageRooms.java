    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       
        try {
             //jtable
                String roomNum = jTextField1.getText();
                String roomType = jComboBox1.getSelectedItem().toString();
                String bedType = jComboBox2.getSelectedItem().toString();
                String roomPrice = jTextField2.getText();
                
                String data[] = {roomNum,roomType,bedType,roomPrice,"Available"};

                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                tblModel.addRow(data);
    
                //database
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/newhotel","root","");
                PreparedStatement ps=con.prepareStatement("insert into managerooms(roomNum,roomType,bedType,roomPrice,status)values(?,?,?,?,?)");
                ps.setString(1,roomNum);
                ps.setString(2, roomType);
                ps.setString(3, bedType);
                ps.setString(4, roomPrice);
                ps.setString(5, "Available");
            
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"Successfully Added Data!");
                
        } catch (SQLException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

